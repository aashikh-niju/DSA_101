
public class Main{
    public static void main(String[] args){
        int a,b,c,temp;
        System.out.println("before swapping........");
        a=10;
        b=20;
        c=30;
        System.out.println("a="+a);
        System.out.println("b="+b);
        System.out.println("c="+c);
        temp=a;
        a=b;
        b=c;
        c=temp;
        System.out.println("after swapping........");
        System.out.println("a="+a);
        System.out.println("b="+b);
        System.out.println("c="+c);

    }

}